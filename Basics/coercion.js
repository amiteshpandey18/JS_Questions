// COERCION -->  Automatic or manually conversion one datatype into another datatype.

console.log(1 + "2"); // 12 --> number is converted into string and concatenated.

console.log("3" - 1); // 2 --> string is converted into number and subtracted.
console.log(true + 1); // 2 --> true is converted into 1 and added.
console.log(false + 1); // 1 --> false is converted into 0 and added.